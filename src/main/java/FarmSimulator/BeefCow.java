package FarmSimulator;

public class BeefCow extends Animal
{
    private String name;
    private int pedegree;
    private int age;
    private int weight;
    private int capacity;

    public BeefCow(String name, int pedegree, int age, int weight, int capacity)
    {
        this.name = name;
        this.pedegree = pedegree;
        this.age = age;
        this.weight = weight;
        this.capacity = capacity;
    }


}



